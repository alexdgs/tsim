    public void mostrarResultados(Object[][] tabla, int op) {
        switch(op) {
            case 1: tmi.mostrar(tabla); break;
            case 2: tvp.mostrar(tabla); break;
            case 3: tc.mostrar(tabla); break;
        }
    }
    
