    // Sets interface elements to match current simulation state
    public void setSimState(int st) {
        switch(st) {
            case DETENIDO:
                tipoJLabel2.setText("Detenido");
                break;
            case EN_EJECUCION:
                tipoJLabel2.setText("En ejecución");
                break;
        }
    }






